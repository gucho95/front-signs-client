const emails = (state = initialState, action) => {
  const { payload, type } = action;
  switch (true) {
    case [EMAIL.REMOVE].includes(type):
      return { ...state, data: state.data.filter((item) => item.id !== payload.id) };
    case [EMAIL.UPDATE].includes(type):
      return {
        ...state,
        data: state.data.map((item) => (item.id === payload.id ? { ...item, payload } : item)),
      };
    default:
      return state;
  }
};

